import UIKit

extension UIColor {
    static func SAMDarkRed() -> UIColor {
        return UIColor(red: 185/255.0, green: 42/255.0, blue: 45/255.0, alpha: 1)
    }
    
    static func SAMLightBlue() -> UIColor {
        return UIColor(red: 91/255.0, green: 190/255.0, blue: 255/255.0, alpha: 1)
    }
}
